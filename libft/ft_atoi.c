int	ft_atoi(const char *str)
{
	unsigned long int	k;
	int					i;
	int					j;

	k = 0;
	i = 0;
	j = 1;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
		|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-')
		j *= -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		k = k * 10 + str[i] - '0';
		if (k == 9223372036854775807 && j > 0)
			return (-1);
		if (k == 9223372036854775807 && j < 0)
			return (0);
		i++;
	}
	return ((int )k * j);
}
